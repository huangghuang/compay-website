        const rawReverse = Object.entries(translations.raw || {}).reduce((acc, [zh, en]) => {
            acc[en] = zh;
            return acc;
        }, {});

        function translateAttr(selector, attr, lang) {
            document.querySelectorAll(selector).forEach(el => {
                const key = el.getAttribute(attr);
                const text = (translations[lang] && translations[lang][key]) || el.dataset.defaultText || '';
                if (text) {
                    el.setAttribute(attr.replace('data-i18n-', ''), text);
                }
            });
        }

        function translateRawTextNodes(root, lang) {
            const map = lang === 'en' ? translations.raw : rawReverse;
            if (!map) return;

            const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
                acceptNode(node) {
                    return node.nodeValue.trim() ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
                }
            });

            while (walker.nextNode()) {
                const node = walker.currentNode;
                const original = node.nodeValue.trim();
                const translated = map[original];
                if (translated) {
                    node.nodeValue = node.nodeValue.replace(original, translated);
                }
            }
        }

        function translatePage(lang) {
            document.querySelectorAll('[data-i18n]').forEach(el => {
                const key = el.getAttribute('data-i18n');
                const text = (translations[lang] && translations[lang][key]) || el.dataset.defaultText || '';
                if (key === 'banner.h1') {
                    el.innerHTML = (text || '').replace(/\n/g, '<br>');
                } else {
                    el.textContent = text;
                }
            });

            translateAttr('[data-i18n-placeholder]', 'data-i18n-placeholder', lang);
            translateAttr('[data-i18n-alt]', 'data-i18n-alt', lang);
            translateAttr('[data-i18n-value]', 'data-i18n-value', lang);
            translateRawTextNodes(document.body, lang);

            document.documentElement.lang = (lang === 'zh' ? 'zh-CN' : 'en');
            localStorage.setItem('site_lang', lang);
        }

        document.addEventListener('DOMContentLoaded', () => {
            // save default texts as fallback
            document.querySelectorAll('[data-i18n]').forEach(el => {
                if (!el.dataset.defaultText) el.dataset.defaultText = el.textContent.trim();
            });

            const saved = localStorage.getItem('site_lang');
            const defaultLang = saved || (navigator.language && navigator.language.startsWith('en') ? 'en' : 'zh');
            translatePage(defaultLang);

            const toggle = document.getElementById('lang-toggle');
            if (toggle) toggle.addEventListener('click', (e) => {
                e.preventDefault();
                const current = localStorage.getItem('site_lang') || (document.documentElement.lang.startsWith('en') ? 'en' : 'zh');
                const next = current === 'zh' ? 'en' : 'zh';
                translatePage(next);
            });
        });

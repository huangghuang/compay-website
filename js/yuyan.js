        function translateAttr(selector, attr, lang) {
            document.querySelectorAll(selector).forEach(el => {
                const key = el.getAttribute(attr);
                const text = (translations[lang] && translations[lang][key]) || el.dataset.defaultText || '';
                if (text) {
                    el.setAttribute(attr.replace('data-i18n-', ''), text);
                }
            });
        }

        function translateRawTextNodes(root, lang) {
            const map = lang === 'en' ? translations.raw : rawReverse;
            if (!map) return;

            const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
                acceptNode(node) {
                    return node.nodeValue.trim() ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
                }
            });

            while (walker.nextNode()) {
                const node = walker.currentNode;
                const original = node.nodeValue.trim();
                const translated = map[original];
                if (translated) {
                    node.nodeValue = node.nodeValue.replace(original, translated);
                }
            }
        }

